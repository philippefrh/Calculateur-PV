function App() {
  const [currentStep, setCurrentStep] = useState('start');
  const [calculationResults, setCalculationResults] = useState(null);
  const [isLoading, setIsLoading] = useState(false);
  const [formData, setFormData] = useState({
    firstName: '',
    lastName: '',
    address: '',
    roofSurface: '',
    roofOrientation: '',
    veluxCount: 0,
    heatingSystem: '',
    waterHeatingSystem: '',
    waterHeatingCapacity: '',
    annualConsumption: '',
    monthlyEdfPayment: '',
    annualEdfPayment: ''
  });

  useEffect(() => {
    console.log('App useEffect - Current step:', currentStep);
  }, [currentStep]);

  const handleStart = () => {
    console.log('handleStart called at:', new Date().toISOString());
    setCurrentStep('personal');
    console.log('State should be changed to: personal');
  };

  const handleNext = () => {
    const steps = ['personal', 'technical', 'heating', 'consumption', 'calculation'];
    const currentIndex = steps.indexOf(currentStep);
    if (currentIndex < steps.length - 1) {
      setIsLoading(true);
      setTimeout(() => {
        setCurrentStep(steps[currentIndex + 1]);
        setIsLoading(false);
      }, 300);
    }
  };

  const handlePrevious = () => {
    const steps = ['start', 'personal', 'technical', 'heating', 'consumption', 'calculation'];
    const currentIndex = steps.indexOf(currentStep);
    if (currentIndex > 0) {
      setIsLoading(true);
      setTimeout(() => {
        setCurrentStep(steps[currentIndex - 1]);
        setIsLoading(false);
      }, 300);
    }
  };

  const handleCalculationComplete = (results) => {
    setCalculationResults(results);
    setTimeout(() => {
      setCurrentStep('results');
    }, 1000);
  };

  const handleNewCalculation = () => {
    setCurrentStep('start');
    setCalculationResults(null);
    setFormData({
      firstName: '',
      lastName: '',
      address: '',
      roofSurface: '',
      roofOrientation: '',
      veluxCount: 0,
      heatingSystem: '',
      waterHeatingSystem: '',
      waterHeatingCapacity: '',
      annualConsumption: '',
      monthlyEdfPayment: '',
      annualEdfPayment: ''
    });
  };

  console.log('Rendering App with currentStep:', currentStep);

  // Loading screen
  if (isLoading) {
    return (
      <div className="App">
        <div className="loading-screen">
          <div className="loading-spinner"></div>
          <p>Chargement...</p>
        </div>
      </div>
    );
  }

  if (currentStep === 'start') {
    return (
      <div className="App">
        <StartScreen onStart={handleStart} />
      </div>
    );
  }

  if (currentStep === 'personal') {
    return (
      <div className="App">
        <PersonalInfoForm 
          formData={formData} 
          setFormData={setFormData} 
          onNext={handleNext} 
          onPrevious={handlePrevious} 
        />
      </div>
    );
  }

  // Pour l'instant, les autres composants ne sont pas encore cr√©√©s
  // Nous les ajouterons dans les prochaines √©tapes
  return (
    <div className="App">
      <div className="error-screen">
        <h2>‚ö†Ô∏è Composant en cours de d√©veloppement</h2>
        <p>√âtape actuelle : {currentStep}</p>
        <button onClick={handleNewCalculation} className="error-button">
          üè† Retour √† l'accueil
        </button>
      </div>
    </div>
  );
}

export default App;